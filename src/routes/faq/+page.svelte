<div class="narrow_container">
	<h1>Frequently Asked Questions</h1>
	<div class="entry">
		<p class="question">How do I create a study in the Lichess app?</p>
		<p class="answer">You'll have to use the <a href="https://lichess.org/study">Lichess web interface</a> to create a study. The Lichess app lacks many features, but they're working on a new one.</p>
	</div>
	<div class="entry">
		<p class="question">Can Chessdriller import Chessable courses?</p>
		<p class="answer">Chessable does not let you export PGN files of their courses. To get a Chessable course into Chessdriller, you'll have to input the lines manually as a Lichess study, or as a PGN in some other software. Sooner or later, you'll want to customize which lines you learn and how deeply you learn them, anyway.</p>
	</div>
	<div class="entry">
		<p class="question">Can Chessdriller import public Lichess studies?</p>
		<p class="answer">Yes. Just click the Lichess study "Clone" button to get a copy on your own account. Your copy will appear automatically in your Chessdriller repertoire page. However, you will surely need to cull the lines manually to make sure only those that you want to learn are included. Many Lichess studies include mistakes to show you what not to play as well as example games, which you would not want to practice with Chessdriller.</p>
	</div>
	<div class="entry">
		<p class="question">I'm getting too many moves to practice?</p>
		<p class="answer">It's tempting to add too many lines and get overwhelmed a couple of days. If you realise that there's too many moves to practice, you can disable a study in the repertoire page or remove a line from your Lichess study. When you add them back later, Chessdriller will remember your progress with those moves.</p>
	</div>
	<div class="entry">
		<p class="question">What does "<i>x</i> due moves" mean in the study screen?</p>
		<p class="answer">There's <i>x</i> moves ready to practice. Play through your repertoire against Chessdriller to get to them. When you first add a move, it will be due immediately. After playing a move correctly for the first time, it will be due again after 10 minutes. Each time the move is played correctly, the interval increases, approximately: 10 minutes, one hour, 8 hours, 1 day, 3 days, 7 days... Playing a move incorrectly resets the interval. This science-based learning technique is called <a href="https://en.wikipedia.org/wiki/Spaced_repetition">spaced repetition</a>.</p>
	</div>
</div>

